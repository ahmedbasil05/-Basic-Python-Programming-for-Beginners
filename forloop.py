#FOR LOOP - iterate for loop for specific number of times

#iterate forward
for i in range(0,10):
    print(i)

#iterate backwards
for i in reversed(range(0,10)):
    print(i)

#iterate using step
for i in range(0,16, 3):  #3 is step here  
    print(i)

#iterate over s string
credit_card = "0123-4567-8910"
for i in credit_card:
    print(i)