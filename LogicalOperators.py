# LOGICAL OPERATOR - used in conditional statements
                # and - True if two or more conditions are true
                # or - True if any one of conditions is true
                # not - True if condition is false, vice versa

temp = 25

# and and or check
if temp > 0 and temp < 30 :
    print("Good Temperature")
else:
    print("hot temperature")    

# not check
x = True
print(not(x))    